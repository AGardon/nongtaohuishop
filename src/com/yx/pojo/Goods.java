package com.yx.pojo;

public class Goods {

	private Integer goodsId;
	private String goodsTitle;
	private String spec;
	private String unit;
	private Category cate;
	private String supplierTitle;
	private Integer priceS;
	private Integer priceP;
	private String state;
	private String property;
	private String image;
	private String url;
	public Integer getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsTitle() {
		return goodsTitle;
	}
	public void setGoodsTitle(String goodsTitle) {
		this.goodsTitle = goodsTitle;
	}
	public String getSpec() {
		return spec;
	}
	public void setSpec(String spec) {
		this.spec = spec;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Category getCate() {
		return cate;
	}
	public void setCate(Category cate) {
		this.cate = cate;
	}
	public String getSupplierTitle() {
		return supplierTitle;
	}
	public void setSupplierTitle(String supplierTitle) {
		this.supplierTitle = supplierTitle;
	}
	public Integer getPriceS() {
		return priceS;
	}
	public void setPriceS(Integer priceS) {
		this.priceS = priceS;
	}
	public Integer getPriceP() {
		return priceP;
	}
	public void setPriceP(Integer priceP) {
		this.priceP = priceP;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getProperty() {
		return property;
	}
	public void setProperty(String property) {
		this.property = property;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
