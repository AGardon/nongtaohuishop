package com.yx.pojo;

public class Category {

	private Integer cateId;
	private String cateTitle;
	private Integer bigCateId;
	private String bigCateTitle;
	public Integer getCateId() {
		return cateId;
	}
	public void setCateId(Integer cateId) {
		this.cateId = cateId;
	}
	public String getCateTitle() {
		return cateTitle;
	}
	public void setCateTitle(String cateTitle) {
		this.cateTitle = cateTitle;
	}
	public Integer getBigCateId() {
		return bigCateId;
	}
	public void setBigCateId(Integer bigCateId) {
		this.bigCateId = bigCateId;
	}
	public String getBigCateTitle() {
		return bigCateTitle;
	}
	public void setBigCateTitle(String bigCateTitle) {
		this.bigCateTitle = bigCateTitle;
	}
	
}
